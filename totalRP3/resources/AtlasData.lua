do  -- Data validation

	for atlasName, atlasInfo in pairs(TRP3_AtlasData) do
		if type(atlasInfo.file) ~= "number" and type(atlasInfo.filename) ~= "string" then
			CallErrorHandler(string.format("atlas %s: missing a valid 'file' or 'filename'", atlasName));
		end

		if type(atlasInfo.width) ~= "number" then
			CallErrorHandler(string.format("atlas %s: 'width' must be a number", atlasName));
		end

		if type(atlasInfo.height) ~= "number" then
			CallErrorHandler(string.format("atlas %s: 'height' must be a number", atlasName));
		end

		if type(atlasInfo.leftTexCoord) ~= "number" then
			CallErrorHandler(string.format("atlas %s: 'leftTexCoord' must be a number", atlasName));
		end

		if type(atlasInfo.rightTexCoord) ~= "number" then
			CallErrorHandler(string.format("atlas %s: 'rightTexCoord' must be a number", atlasName));
		end

		if type(atlasInfo.topTexCoord) ~= "number" then
			CallErrorHandler(string.format("atlas %s: 'topTexCoord' must be a number", atlasName));
		end

		if type(atlasInfo.bottomTexCoord) ~= "number" then
			CallErrorHandler(string.format("atlas %s: 'bottomTexCoord' must be a number", atlasName));
		end

		if type(atlasInfo.tilesHorizontally) ~= "boolean" then
			CallErrorHandler(string.format("atlas %s: 'tilesHorizontally' must be a boolean", atlasName));
		end

		if type(atlasInfo.tilesVertically) ~= "boolean" then
			CallErrorHandler(string.format("atlas %s: 'tilesVertically' must be a boolean", atlasName));
		end
	end
end
